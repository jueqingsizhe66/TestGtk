/**
 *  * final三大特性：
 *   1final变量
 *   2final方法
 *   3final类
 * 什么时候使用final？
 *   1某些变量不允许被修改  比如圆周率 税率等  可以在父类中定义成final类
 *   2某些方法 你已经写的很漂亮了  不希望被子类进行再次修改   可以定义成final函数类
 *   3某些类你不希望被别人继承  觉得他实在是太重要了  不允许别的类作为他的子类 也就是断子绝孙
 * 使用final需要注意些什么？
 *   1定义final变量时 要用   XX_XX_XX
 *   2final变量一定要进行初始化    并且初始化玩之后不能进行再次赋值
 */
